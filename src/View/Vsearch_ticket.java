    private void btn_printActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_printActionPerformed
        // TODO add your handling code here:
         
        
         HashMap a = new HashMap();
       a.put("tp", Integer.parseInt(txt_customerTp.getText()));
       a.put("ticketId", Integer.parseInt( txt_ticketNo.getText()));
       
       pnl_report.removeAll();
       pnl_report.repaint();
       pnl_report.revalidate();
       
       try{
          JasperDesign jdesign = JRXmlLoader.load("C:\\Users\\Ockersz\\Desktop\\airline\\src\\View\\ticket.jrxml");
       JasperReport jreport  = JasperCompileManager.compileReport(jdesign);
       
          JasperPrint jprint = JasperFillManager.fillReport(jreport,a, MyDbConnection.getConnection());
          
          JRViewer v = new JRViewer(jprint);
          pnl_report.setLayout(new BorderLayout());
          pnl_report.add(v);
       }
       catch(JRException e)
       {
          JOptionPane.showMessageDialog(null, e.getMessage());
       }
    
    }//GEN-LAST:event_btn_printActionPerformed

