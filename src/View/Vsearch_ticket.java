    private void btn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchActionPerformed
          if(txt_custp.getText().isEmpty())
       {
           JOptionPane.showMessageDialog(null,"Firstname cannot be blank");
       }
          else if(!txt_custp.getText().matches("\\d+"))
          {
              JOptionPane.showMessageDialog(null,"Contact cannot contain letters");
          }
          else
          {
              ResultSet rs = CTicket.searchTicket(txt_custp.getText());
              
              DefaultTableModel dtm = new DefaultTableModel();
            dtm.addColumn("Customer TP");
            dtm.addColumn("Customer Name");
            dtm.addColumn("Ticket ID");
            dtm.addColumn("Source");
            dtm.addColumn("Destination");
            dtm.addColumn("Arrival Time");
            dtm.addColumn("Departure Time");
            dtm.addColumn("Number of Seats");
            dtm.addColumn("Tickets Price");
            dtm.addColumn("Date");

        try{
            while(rs.next()){
                String[] values = new String[10];
                values[0] = Integer.toString(rs.getInt(1));
                values[1] = rs.getString(2);
                values[2] = Integer.toString(rs.getInt(3));
                values[3] = rs.getString(6);
                values[4] = rs.getString(7);
                values[5] = rs.getString(8);
                values[6] = rs.getString(9);
                values[7] = Integer.toString(rs.getInt(4));
                values[8] = Integer.toString(rs.getInt(5));
                values[9] = rs.getDate(10).toString();

                
                dtm.addRow(values);
            }
            tbl_ticket.setModel(dtm);

        }
        catch(SQLException e)
        {
            e.printStackTrace();
        }
          }
          
          
    }//GEN-LAST:event_btn_searchActionPerformed

